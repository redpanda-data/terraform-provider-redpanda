func TestCompareBufModules(t *testing.T) {
	pinned := map[string]string{
		"buf.build/redpandadata/cloud":     "bea2c0f11c89",
		"buf.build/redpandadata/dataplane": "5d8d126492a6",
	}
	current := map[string]string{
		"buf.build/redpandadata/cloud":     "deadbeef0000", // changed
		"buf.build/redpandadata/dataplane": "5d8d126492a6", // same
		"buf.build/redpandadata/common":    "newmodule000", // new in go.mod
	}
	msgs := CompareBufModules(pinned, current)
	if len(msgs) != 2 {
		t.Fatalf("expected 2 messages, got %d: %v", len(msgs), msgs)
	}
	joined := strings.Join(msgs, "\n")
	if !strings.Contains(joined, "redpandadata/cloud") || !strings.Contains(joined, "bump-deps") {
		t.Errorf("messages missing expected content:\n%s", joined)
	}
	if !strings.Contains(joined, "redpandadata/common") {
		t.Errorf("new module not reported:\n%s", joined)
	}
}

func mustRun(t *testing.T, dir, name string, args ...string) {
	t.Helper()
	cmd := exec.CommandContext(context.Background(), name, args...)
	cmd.Dir = dir
	if out, err := cmd.CombinedOutput(); err != nil {
		t.Fatalf("%s %v: %v: %s", name, args, err, out)
	}
}

func mustOut(t *testing.T, dir, name string, args ...string) string {
	t.Helper()
	cmd := exec.CommandContext(context.Background(), name, args...)
	cmd.Dir = dir
	out, err := cmd.Output()
	if err != nil {
		t.Fatalf("%s %v: %v", name, args, err)
	}
	return strings.TrimSpace(string(out))
}
