func TestReadWriteRoundTrip(t *testing.T) {
	dir := t.TempDir()
	path := filepath.Join(dir, "buf_dependencies.yaml")

	want := &Deps{
		Cloudv2: Source{
			Remote: "github.com/redpanda-data/cloudv2",
			SHA:    "aea3fe56605ea15169f262252a571808417f164e",
		},
		Console: Source{
			Remote: "github.com/redpanda-data/console",
			SHA:    "4a8e3db68f5569812b65410afe37538f72728ac5",
		},
		BufModules: map[string]string{
			"buf.build/redpandadata/cloud":     "bea2c0f11c89",
			"buf.build/redpandadata/dataplane": "5d8d126492a6",
		},
	}
	if err := Write(path, want); err != nil {
		t.Fatalf("Write: %v", err)
	}
	got, err := Read(path)
	if err != nil {
		t.Fatalf("Read: %v", err)
	}
	if !reflect.DeepEqual(got, want) {
		t.Fatalf("round-trip mismatch:\n got: %+v\nwant: %+v", got, want)
	}

	// Header is preserved verbatim.
	data, err := os.ReadFile(filepath.Clean(path))
	if err != nil {
		t.Fatalf("read raw: %v", err)
	}
	if !strings.HasPrefix(string(data), "# Code generated by cmd/schemagen -update-deps. DO NOT EDIT.") {
		t.Fatalf("header missing from %q", string(data))
	}
}

func TestReadMissingSHA(t *testing.T) {
	dir := t.TempDir()
	path := filepath.Join(dir, "buf_dependencies.yaml")
	if err := os.WriteFile(path, []byte("cloudv2:\n  remote: foo\nconsole:\n  remote: bar\n"), 0o600); err != nil {
		t.Fatalf("seed: %v", err)
	}
	if _, err := Read(path); err == nil {
		t.Fatal("expected error for empty SHA, got nil")
	}
}

func TestAssertCheckoutAt(t *testing.T) {
	if _, err := exec.LookPath("git"); err != nil {
		t.Skip("git not available")
	}
	dir := t.TempDir()
	mustRun(t, dir, "git", "init", "-q", "-b", "main")
	mustRun(t, dir, "git", "config", "user.email", "t@example.test")
	mustRun(t, dir, "git", "config", "user.name", "t")
	mustRun(t, dir, "git", "commit", "-q", "--allow-empty", "-m", "init")

	head := mustOut(t, dir, "git", "rev-parse", "HEAD")

	if err := AssertCheckoutAt(dir, head, "test"); err != nil {
		t.Fatalf("matching SHA: %v", err)
	}
	err := AssertCheckoutAt(dir, "0123456789abcdef0123456789abcdef01234567", "test")
	if err == nil {
		t.Fatal("expected mismatch error, got nil")
	}
	if !strings.Contains(err.Error(), "bump-deps") {
		t.Fatalf("error should hint at bump-deps: %v", err)
	}
}

func TestLoadBufModulesFromGoMod(t *testing.T) {
	dir := t.TempDir()
	goMod := filepath.Join(dir, "go.mod")
	body := `module example

go 1.25

require (
	buf.build/gen/go/redpandadata/cloud/grpc/go v1.6.2-20260527154152-bea2c0f11c89.1
	buf.build/gen/go/redpandadata/cloud/protocolbuffers/go v1.36.11-20260527154152-bea2c0f11c89.1
	buf.build/gen/go/redpandadata/dataplane/grpc/go v1.6.1-20260429204409-5d8d126492a6.1
	buf.build/gen/go/redpandadata/dataplane/protocolbuffers/go v1.36.11-20260429204409-5d8d126492a6.1
	buf.build/gen/go/redpandadata/core/protocolbuffers/go v1.36.11-20260410204044-2088239db14c.1
)
`
	if err := os.WriteFile(goMod, []byte(body), 0o600); err != nil {
		t.Fatalf("seed: %v", err)
	}
	got, err := LoadBufModulesFromGoMod(goMod)
	if err != nil {
		t.Fatalf("LoadBufModulesFromGoMod: %v", err)
	}
	want := map[string]string{
		"buf.build/redpandadata/cloud":     "bea2c0f11c89",
		"buf.build/redpandadata/dataplane": "5d8d126492a6",
		"buf.build/redpandadata/core":      "2088239db14c",
	}
	if len(got) != len(want) {
		t.Fatalf("len mismatch: got %d want %d (%v)", len(got), len(want), got)
	}
	for k, v := range want {
		if got[k] != v {
			t.Errorf("%s: got %q want %q", k, got[k], v)
		}
	}
}

