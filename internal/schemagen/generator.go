func scanExprImports(expr string, needed map[string]bool, _ string) {
	for prefix, importPath := range importPrefixes {
		if strings.Contains(expr, prefix) {
			needed[importPath] = true
		}
	}
}

func renderAttr(attr SchemaAttr, indent int) string {
	prefix := strings.Repeat("\t", indent)
	inner := prefix + "\t"
	var b strings.Builder

	fmt.Fprintf(&b, "%s%q: schema.%s{\n", prefix, attr.Name, attr.AttrType)

	if attr.Description != "" {
		fmt.Fprintf(&b, "%sDescription: %q,\n", inner, attr.Description)
	}
	if attr.Required {
		fmt.Fprintf(&b, "%sRequired: true,\n", inner)
	}
	if attr.Optional {
		fmt.Fprintf(&b, "%sOptional: true,\n", inner)
	}
	if attr.Computed {
		fmt.Fprintf(&b, "%sComputed: true,\n", inner)
	}
	if attr.Sensitive {
		fmt.Fprintf(&b, "%sSensitive: true,\n", inner)
	}
	if attr.WriteOnly {
		fmt.Fprintf(&b, "%sWriteOnly: true,\n", inner)
	}
	if attr.DeprecationMessage != "" {
		fmt.Fprintf(&b, "%sDeprecationMessage: %q,\n", inner, attr.DeprecationMessage)
	}
	if attr.Default != "" {
		fmt.Fprintf(&b, "%sDefault: %s,\n", inner, attr.Default)
	}
	if attr.PlanModifiers != "" {
		fmt.Fprintf(&b, "%sPlanModifiers: %s,\n", inner, attr.PlanModifiers)
	}
	if attr.Validators != "" {
		fmt.Fprintf(&b, "%sValidators: %s,\n", inner, attr.Validators)
	}
	if attr.ElementType != "" {
		fmt.Fprintf(&b, "%sElementType: %s,\n", inner, attr.ElementType)
	}

	if len(attr.NestedAttrs) > 0 {
		switch attr.AttrType {
		case AttrTypeListNested, AttrTypeSetNested, AttrTypeMapNested:
			fmt.Fprintf(&b, "%sNestedObject: schema.NestedAttributeObject{\n", inner)
			fmt.Fprintf(&b, "%s\tAttributes: map[string]schema.Attribute{\n", inner)
			for i := range attr.NestedAttrs {
				b.WriteString(renderAttr(attr.NestedAttrs[i], indent+3))
			}
			fmt.Fprintf(&b, "%s\t},\n", inner)
			fmt.Fprintf(&b, "%s},\n", inner)
		default:
			fmt.Fprintf(&b, "%sAttributes: map[string]schema.Attribute{\n", inner)
			for i := range attr.NestedAttrs {
				b.WriteString(renderAttr(attr.NestedAttrs[i], indent+2))
			}
			fmt.Fprintf(&b, "%s},\n", inner)
		}
	}

	fmt.Fprintf(&b, "%s},\n", prefix)
	return b.String()
}
