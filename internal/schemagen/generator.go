// TemplateData holds all data needed to render the schema template.
type TemplateData struct {
	License        string
	Package        string
	FuncName       string
	SchemaType     string
	Description    string
	NeedsContext   bool
	Imports        []string
	Attributes     []SchemaAttr
	HasTimeouts    bool
	IsDatasource   bool
	TimeoutsCreate bool
	TimeoutsRead   bool
	TimeoutsUpdate bool
	TimeoutsDelete bool
	Version        int64
}

// Generate produces formatted Go source code for a schema file.
func Generate(data TemplateData) ([]byte, error) {
	funcMap := template.FuncMap{
		"renderAttr": renderAttr,
	}

	tmpl, err := template.New("schema").Funcs(funcMap).Parse(schemaTmpl)
	if err != nil {
		return nil, fmt.Errorf("failed to parse schema template: %w", err)
	}

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, data); err != nil {
		return nil, fmt.Errorf("failed to execute schema template: %w", err)
	}

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return buf.Bytes(), fmt.Errorf("failed to format generated code: %w\n%s", err, buf.String())
	}

	return formatted, nil
}

// ResolveImports determines the required imports from the schema attributes
// and configuration.
func ResolveImports(attrs []SchemaAttr, schemaType string, hasTimeouts bool, extraImports []string) []string {
	needed := make(map[string]bool)

	needed["context"] = true
	if schemaType == SchemaTypeDatasource {
		needed["github.com/hashicorp/terraform-plugin-framework/datasource/schema"] = true
	} else {
		needed["github.com/hashicorp/terraform-plugin-framework/resource/schema"] = true
	}

	scanImports(attrs, needed, schemaType)

	if hasTimeouts {
		if schemaType == SchemaTypeDatasource {
			needed["github.com/hashicorp/terraform-plugin-framework-timeouts/datasource/timeouts"] = true
		} else {
			needed["github.com/hashicorp/terraform-plugin-framework-timeouts/resource/timeouts"] = true
		}
	}

	for _, imp := range extraImports {
		needed[imp] = true
	}

	imports := make([]string, 0, len(needed))
	for imp := range needed {
		imports = append(imports, imp)
	}
	sort.Strings(imports)
	return imports
}

func scanImports(attrs []SchemaAttr, needed map[string]bool, schemaType string) {
	for i := range attrs {
		attr := &attrs[i]
		if attr.ElementType != "" {
			needed["github.com/hashicorp/terraform-plugin-framework/types"] = true
		}
		for _, expr := range []string{attr.PlanModifiers, attr.Validators, attr.Default} {
			if expr == "" {
				continue
			}
			scanExprImports(expr, needed, schemaType)
		}
		scanImports(attr.NestedAttrs, needed, schemaType)
	}
}

