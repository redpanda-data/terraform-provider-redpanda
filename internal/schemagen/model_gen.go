// ModelData is the template input for the resource/data-source model file.
type ModelData struct {
	License      string
	Package      string
	TypeName     string
	ResourceName string
	SchemaType   string
	Imports      []string
	Fields       []ModelField
	HasTimeouts  bool
	NestedTypes  []NestedType
	Converters   []Converter
	HasNested    bool

	// SubConverters emits free-standing Decode*/ToObject helpers for nested
	// types that are NOT at the root level. Required by NestedPreserveSubBlock
	// in the conv_gen template so that 2-level-deep sensitive fields can be
	// restored from prev-state.
	SubConverters []SubConverter

	EmitMinimal    bool
	MinimalAssigns []MinimalAssign
	IDFieldType    string
	HasTimeoutsArg bool
}

// SubConverter generates a free-standing Decode*/ToObject pair for a nested
// type whose parent is NOT the root model struct. Used by NestedPreserveSubBlock.
type SubConverter struct {
	// DecodeFuncName is the name of the decode function, e.g. "DecodeClientOptionsTLSSettings".
	DecodeFuncName string

	// ToObjectFuncName is the ToObject encode function, e.g. "ClientOptionsTLSSettingsToObject".
	ToObjectFuncName string

	// ParentType is the Go type of the parent struct, e.g. "ClientOptionsModel".
	ParentType string

	// FieldName is the Go field on the parent that holds the sub-type, e.g. "TLSSettings".
	FieldName string

	// NestedType is the Go type of the nested struct, e.g. "ClientOptionsTLSSettingsModel".
	NestedType string

	// AttrTypesFunc is the AttrTypes helper function, e.g. "ClientOptionsTLSSettingsAttrTypes".
	AttrTypesFunc string
}

// ModelField is a single struct field on the generated model.
type ModelField struct {
	Name string
	Type string
	Tag  string
}

// MinimalAssign is one field's RHS in GenerateMinimalResourceModel — either
// the id parameter, the timeouts parameter, an explicit minimal_default
// expression, or a typed null appropriate to the field's AttrType.
type MinimalAssign struct {
	GoName string
	Expr   string
}

