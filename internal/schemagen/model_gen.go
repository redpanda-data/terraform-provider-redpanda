// GenerateModel renders the resource/data-source model file from the merged
// schema attributes. Emits the root struct, any nested typed struct types,
// their AttrType tables, and root-level converters.
func GenerateModel(attrs []SchemaAttr, hasTimeouts bool, schemaType, pkgName, resourceName string) ([]byte, error) {
	typeName := ModelTypeResource
	if schemaType == SchemaTypeDatasource {
		typeName = ModelTypeDatasource
	}

	imports := map[string]bool{
		"github.com/hashicorp/terraform-plugin-framework/types": true,
	}
	fields := make([]ModelField, 0, len(attrs))
	for i := range attrs {
		a := &attrs[i]
		fields = append(fields, ModelField{
			Name: toGoFieldName(a.Name),
			Type: modelGoTypeForAttr(a.AttrType),
			Tag:  a.Name,
		})
	}

	if hasTimeouts {
		fields = append(fields, ModelField{
			Name: "Timeouts",
			Type: "timeouts.Value",
			Tag:  "timeouts",
		})
		if schemaType == SchemaTypeDatasource {
			imports["github.com/hashicorp/terraform-plugin-framework-timeouts/datasource/timeouts"] = true
		} else {
			imports["github.com/hashicorp/terraform-plugin-framework-timeouts/resource/timeouts"] = true
		}
	}

	sort.Slice(fields, func(i, j int) bool {
		return fields[i].Name < fields[j].Name
	})

	prefix := ""
	if schemaType == SchemaTypeDatasource {
		prefix = DatasourcePrefix
	}
	nested, err := collectNestedTypes(attrs, prefix)
	if err != nil {
		return nil, err
	}
	converters := collectConverters(attrs, prefix)
	subConverters := collectSubConverters(attrs, prefix)
	hasNested := len(nested) > 0 || len(converters) > 0

	if hasNested {
		imports["context"] = true
		imports["github.com/hashicorp/terraform-plugin-framework/attr"] = true
		imports["github.com/hashicorp/terraform-plugin-framework/diag"] = true

		for _, c := range converters {
			if c.Kind == "single" {
				imports["github.com/hashicorp/terraform-plugin-framework/types/basetypes"] = true
				break
			}
		}
		if len(subConverters) > 0 {
			imports["github.com/hashicorp/terraform-plugin-framework/types/basetypes"] = true
		}
	}

	importList := make([]string, 0, len(imports))
	for imp := range imports {
		importList = append(importList, imp)
	}
	sort.Strings(importList)

	data := ModelData{
		License:       LicenseHeader(),
		Package:       pkgName,
		TypeName:      typeName,
		ResourceName:  resourceName,
		SchemaType:    schemaType,
		Imports:       importList,
		Fields:        fields,
		HasTimeouts:   hasTimeouts,
		NestedTypes:   nested,
		Converters:    converters,
		SubConverters: subConverters,
		HasNested:     hasNested,
	}

	if schemaType != SchemaTypeDatasource {
		assigns, idType, ok, err := planMinimalAssigns(attrs, hasTimeouts, prefix)
		if err != nil {
			return nil, err
		}
		if ok {
			data.EmitMinimal = true
			data.MinimalAssigns = assigns
			data.IDFieldType = idType
			data.HasTimeoutsArg = hasTimeouts
		}
	}

	tmpl, err := template.New("model").Parse(modelTmpl)
	if err != nil {
		return nil, fmt.Errorf("parse model template: %w", err)
	}
	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, data); err != nil {
		return nil, fmt.Errorf("execute model template: %w", err)
	}
	src, err := format.Source(buf.Bytes())
	if err != nil {
		return buf.Bytes(), fmt.Errorf("format generated model: %w\n%s", err, buf.String())
	}
	return src, nil
}

func toGoFieldName(name string) string {
	abbreviations := map[string]string{
		"id":   "ID",
		"url":  "URL",
		"uri":  "URI",
		"api":  "API",
		"http": "HTTP",
		"json": "JSON",
		"xml":  "XML",
		"sql":  "SQL",
		"ssh":  "SSH",
		"tls":  "TLS",
		"ssl":  "SSL",
		"aws":  "AWS",
		"gcp":  "GCP",
		"vpc":  "VPC",
		"dns":  "DNS",
		"arn":  "ARN",
		"cmr":  "CMR",
		"wo":   "WO",
	}

	parts := strings.Split(name, "_")
	var result strings.Builder
	for _, part := range parts {
		if part == "" {
			continue
		}
		if abbr, ok := abbreviations[strings.ToLower(part)]; ok {
			result.WriteString(abbr)
			continue
		}
		runes := []rune(part)
		runes[0] = unicode.ToUpper(runes[0])
		result.WriteString(string(runes))
	}
	return result.String()
}

func planMinimalAssigns(attrs []SchemaAttr, hasTimeouts bool, datasourcePrefix string) (assigns []MinimalAssign, idType string, ok bool, err error) {
	var idField *SchemaAttr
	for i := range attrs {
		if attrs[i].Name == "id" {
			idField = &attrs[i]
			break
		}
	}
	if idField == nil {
		return nil, "", false, nil
	}
	idType = modelGoTypeForAttr(idField.AttrType)

	for i := range attrs {
		a := &attrs[i]
		expr, exprErr := minimalAssignExpr(a, datasourcePrefix)
		if exprErr != nil {
			return nil, "", false, fmt.Errorf("minimal assign for %q: %w", a.Name, exprErr)
		}
		assigns = append(assigns, MinimalAssign{
			GoName: toGoFieldName(a.Name),
			Expr:   expr,
		})
	}
	if hasTimeouts {
		assigns = append(assigns, MinimalAssign{GoName: "Timeouts", Expr: "timeout"})
	}
	sort.Slice(assigns, func(i, j int) bool { return assigns[i].GoName < assigns[j].GoName })
	return assigns, idType, true, nil
}

func minimalAssignExpr(a *SchemaAttr, datasourcePrefix string) (string, error) {
	if a.Name == "id" {
		return "id", nil
	}
	if a.MinimalDefault != "" {
		return a.MinimalDefault, nil
	}
	return NullExpr(a, NullExprOptions{HelperPrefix: datasourcePrefix})
}
